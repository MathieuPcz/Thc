					<p><strong>A Troyes. Tricasses de Troyes - Vitry-sur-Seine : 6-4 </strong><br><br>
Gardien de buts : Valentin Durand (1) Joueurs : Quentin Amiot (45), Sylvain Caherine (58), Julien Chaton (15)-1 ass., Paul Janny (11), Pierre Lefebvre (23), Alexandre Legault (26)-2 buts + 1 ass., William Legault (58)-1but, Jean-Marc Manjard (24)-1 but, Sébastien Novello (20)-3 ass, Thomas Parent (17)-1 ass, Alexis Rouhaud (25)-2 but., Nicolas Vieville (22) et Luc Walter (11) <br> <br>


L'équipe seniors des Tricasses du Troyes Hockey Club, a une nouvelle fois brillé lors du match « coupe trophée loisirs » de samedi dernier au cours duquel elle rencontrait son challenger Vitry-sur-Seine. <br><br>
La brutalité des adversaires n'aura pas eu raison de la finesse du jeu troyen. En effet, cette équipe d'anciens joueurs de haut niveau comptait sur sa grossière agressivité pour dominer et gagner le match. Mais les Tricasses étaient prêts et la puissance de leur jeu leur permit de prendre le dessus malgré une première période difficile qui les laissait rentrer bredouilles au vestiaire. <br> <br>
La pause permit de remettre les choses en ordre, à la 21e minute, le Troyen Alexis Rouhaud ouvrit le score puis quelques minutes plus tard, Jean-Marc Manjard confirma la marque. La défense négocia parfaitement sa seconde séance de travail et ne laissa pas les Vitriots marquer durant cette période. C'est sur le score de 2-1 que les Tricasses regagnaient le vestiaire. <br><br>
Dépassé en début de 3e période, le gardien troyen ouvrit les portes de sa cage en laissant passer 3 buts en l'espace d'une minute. Mais l'équipe se ressaisissait et revenait rapidement dans la partie. Le défenseur William Legault amorça le colmatage de la brèche puis suivirent trois autres buts encaissés par l'adversaire à qui la défense des Tricasses ne laissa plus aucune chance. <br><br>
L'attitude des visiteurs laissa perplexes bon nombre des spectateurs mais les jeunes joueurs troyens prouvèrent une nouvelle fois que le hockey sur glace est un jeu habile et intelligent où la violence n'a pas sa place, en marquant 6 buts avec agilité et subtilité tout en respectant l'adversaire et les arbitres. <br><br>
Ce fut une véritable satisfaction lors du coup de sifflet final de constater que le tableau affichait la réussite des Troyens qui l'emportaient sur le score de 6-4. Cette 3e victoire confirme la place des Tricasses en haut du tableau !</p>

